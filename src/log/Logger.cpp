#include "log/Logger.h"
#include <boost/log/expressions.hpp>
#include <boost/log/support/date_time.hpp>
#include <boost/log/utility/setup/common_attributes.hpp>
#include <boost/algorithm/string.hpp>

namespace logging = boost::log;
namespace src = boost::log::sources;
namespace expr = boost::log::expressions;
namespace sinks = boost::log::sinks;
namespace attrs = boost::log::attributes;

BOOST_LOG_GLOBAL_LOGGER_INIT(Robot::GlobalLogger, Robot::TrivialLogger) {
    Robot::TrivialLogger lg;
    // Add attributes
    lg.add_attribute("LineID", attrs::counter<unsigned int>(1)); // lines are sequentially numbered
    lg.add_attribute("TimeStamp", attrs::local_clock()); // each log line gets a timestamp
    return lg;
}
